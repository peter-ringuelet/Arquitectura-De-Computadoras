hola db 23H

ORG 3000H
UN_DIGITO: CMP CL, 9H
JZ UNICO
JNS VARIOS_DIGITOS
UNICO: MOV CH, 00H
JMP FIN
VARIOS_DIGITOS: MOV CH, 0FFH
FIN: RET

ORG 2000H
MOV DL,0
MOV BX, OFFSET MSJ
MOV AL, OFFSET OP1 - OFFSET MSJ
INT 7
MOV BX, OFFSET OP1
INT 6
MOV BX, OFFSET OP2
INT 6
SUB OP1, 30H 
SUB OP2, 30H
MOV CL, OP1
ADD CL, OP2
MOV RES, CL
CALL UN_DIGITO
CMP CH, 0FFH
JNZ PRINT_UNICO
CMP CL, 00AH
JNZ PRINT_VARIOS
MOV AL,1
MOV RES,31H
MOV BX, OFFSET RES
INT 7
MOV RES, 30H
INT 7
JMP FINN
PRINT_VARIOS: DEC CL
INC DL
CMP CL, 00AH
JNZ PRINT_VARIOS
MOV BX, OFFSET RES
MOV RES,31H
MOV AL,1
INT 7
MOV RES, DL
ADD RES, 30H
INT 7
JMP FINN
PRINT_UNICO: ADD RES, 30H
MOV BX, OFFSET RES
MOV AL,1
INT 7
FINN: INT 0
END